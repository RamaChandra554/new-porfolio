// Generate project cards
function generateProjectCards() {
    const container = document.querySelector('.projects-container');
    
    projectsData.forEach(project => {
        const card = document.createElement('div');
        card.className = `project-card glass active`;
        card.setAttribute('data-category', project.category);
        if (project.subcategory) {
            card.setAttribute('data-subcategory', project.subcategory);
        }
        
        card.innerHTML = `
            <div class="card border-0 position-relative overflow-hidden">
                <img src="${project.image}" alt="${project.title}" class="project-image">
                <a href="#" class="stretched-link" data-bs-toggle="modal" 
                   data-bs-target="#project-modal" onclick="populateProjectModal(${project.id})"></a>
                <div class="card-img-overlay-custom text-center">
                    <div class="certificate-title">${project.title}</div>
                    <div class="certificate-types">${project.tags.join(', ')}</div>
                </div>
            </div>
            <div class="project-info">
                <h3>${project.title}</h3>
                <p>${project.description}</p>
                <div class="project-tags">
                    ${project.tags.map(tag => `<span class="tag">${tag}</span>`).join('')}
                </div>
                <a href="${project.link}" class="btn btn-primary" target="_blank">
                    ${project.category === 'figma' ? 'View Prototype' : 'Live Demo'}
                </a>
            </div>
        `;
        container.appendChild(card);
    });
}

// Populate project modal
function populateProjectModal(projectId) {
    const project = projectsData.find(p => p.id === projectId);
    if (project) {
        document.getElementById('modal-project-image').src = project.image;
        document.getElementById('modal-project-title').textContent = project.title;
        document.getElementById('modal-project-description').textContent = project.description;
        document.getElementById('modal-project-link').href = project.link;
        
        const tagsContainer = document.getElementById('modal-project-tags');
        tagsContainer.innerHTML = project.tags.map(tag => 
            `<span class="tag">${tag}</span>`
        ).join('');
        
        document.getElementById('modal-project-category').textContent = 
            `Category: ${project.category.charAt(0).toUpperCase() + project.category.slice(1)}` + 
            (project.subcategory ? ` | ${project.subcategory}` : '');
    }
}

// Initialize projects after DOM loads
document.addEventListener('DOMContentLoaded', () => {
    generateProjectCards();
    
    // Re-initialize your existing filter script
    const filterButtons = document.querySelectorAll('.filter-btn');
    const subfilterButtons = document.querySelectorAll('.subfilter-btn');
    const designSubfilters = document.querySelector('.design-subfilters');

    filterButtons.forEach(button => {
        button.addEventListener('click', () => {
            filterButtons.forEach(btn => btn.classList.remove('active'));
            button.classList.add('active');
            const filter = button.dataset.filter;

            // Toggle design subfilters
            designSubfilters.classList.toggle('active', filter === 'design');
            if (filter !== 'design') {
                subfilterButtons.forEach(btn => btn.classList.remove('active'));
            }

            // Filter projects using current DOM elements
            document.querySelectorAll('.project-card').forEach(card => {
                card.classList.toggle('active', 
                    filter === 'all' || 
                    card.dataset.category === filter
                );
            });
        });
    });

    subfilterButtons.forEach(button => {
        button.addEventListener('click', () => {
            subfilterButtons.forEach(btn => btn.classList.remove('active'));
            button.classList.add('active');
            const subfilter = button.dataset.subfilter;

            document.querySelectorAll('.project-card').forEach(card => {
                if (card.dataset.category === 'design') {
                    card.classList.toggle('active', 
                        subfilter === 'all-design' || 
                        card.dataset.subcategory === subfilter
                    );
                }
            });
        });
    });
});